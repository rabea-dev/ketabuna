export function getBookBySlug(slug: string): Book | undefined {
  return books.find(b => b.slug === slug)
}

export function getFeaturedBooks(): Book[] {
  return books.filter(b => b.featured && b.status === 'published')
}

export function getPublishedBooks(): Book[] {
  return books.filter(b => b.status === 'published')
}

export function getRelatedBooks(book: Book, limit = 3): Book[] {
  const related = book.relatedBooks || []
  return books
    .filter(b => b.id !== book.id && b.status === 'published' && related.includes(b.id))
    .slice(0, limit)
}
